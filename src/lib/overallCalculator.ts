  //Function to calculate the overall rating based on player attributes and position
  export function calculateOverall(player: PlayerData | null): number {
    if (!player) return 0;
    
    const positionCode = player.PLYR_POSITION;
    if (!positionCode) return 0;
    
    const weights = positionToWeights[positionCode];
    if (!weights) return 0;
    
    let overall = weights.init || 0; 
    
    // Loop through all the weights and add the corresponding player attributes
    for (const [key, weight] of Object.entries(weights)) {
      if (key === "init") continue; // Skip the initial value (already taken care of above)
      
      const attributeValue = Number(player[key as keyof PlayerData] || 0);
      overall += attributeValue * weight;
    }
    
    // Round to the nearest integer and ensure it's between 0 and 125. Cause we crazy like that.
    return Math.max(0, Math.min(125, Math.round(overall)));
  }
  
    //Function to calculate the difference between the calculated overall and the stored overall rating
  export function getOverallDifference(player: PlayerData | null): number {
    if (!player) return 0;
    
    const calculatedOvr = calculateOverall(player);
    const storedOvr = Number(player.PLYR_OVERALLRATING || 0);
    
    return calculatedOvr - storedOvr;
  }